#include <stdio.h>
int main() {
    int n;
    printf("Enter a number: ");
    scanf("%d", &n);

    // Print upper half of the pattern
    for (int i =1; i<=2*n-1; i++) 
    {
        int k;
        k=n;
        if(i<=n)
        {
            for(int j=1; j<=2*n-1; j++)
            {
                printf("%d ",k);
                if(i>j)
                {
                    k=k-1;
                }
                if((i+j)>=(2*n))
                {
                    k=k+1;
                }
            }
        printf("\n");
        }
        else //to print lower half pattern
        {
            for(int j=1;j<=2*n-1;j++)
            {
                printf("%d ",k);
                if((i+j)<(2*n))
                {
                    k=k-1;
                }
                if(i<=j)
                {
                    k=k+1;
                }
            }
        printf("\n");
        }
    }
    return 0;
}

